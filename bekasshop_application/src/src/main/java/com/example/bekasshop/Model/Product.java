package com.example.bekasshop.Model;

public class Product {
    private String id;
    private String name;
    private String description;
    private String image;
    private int price;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getDescription() {
        return description;
    }

    public String getImage() {
        return image;
    }

    public int getPrice() {
        return price;
    }
}
